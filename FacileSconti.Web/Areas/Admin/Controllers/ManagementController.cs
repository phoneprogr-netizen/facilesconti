    [HttpPost]
    [ValidateAntiForgeryToken]
    public async Task<IActionResult> NewCustomer(AdminCustomerFormViewModel model, CancellationToken cancellationToken)
    {
        await PopulateOwnerUsersAsync(model, model.OwnerUserId, cancellationToken);
        model.ExistingLogoPath = null;
        if (!ModelState.IsValid)
            return View("CustomerForm", model);

        string? autoGeneratedPassword = null;
        var ownerUserId = model.OwnerUserId;
        if (string.IsNullOrWhiteSpace(ownerUserId))
        {
            var existingUser = await _userManager.FindByEmailAsync(model.Email);
            if (existingUser is not null)
            {
                ownerUserId = existingUser.Id;
            }
            else
            {
                autoGeneratedPassword = GenerateTemporaryPassword();
                var generatedUser = new ApplicationUser
                {
                    UserName = model.Email,
                    Email = model.Email,
                    FirstName = "Owner",
                    LastName = model.Name,
                    UserType = UserType.Customer
                };

                var result = await _userManager.CreateAsync(generatedUser, autoGeneratedPassword);
                if (!result.Succeeded)
                {
                    foreach (var error in result.Errors)
                        ModelState.AddModelError(string.Empty, error.Description);

                    await PopulateOwnerUsersAsync(model, null, cancellationToken);
                    return View("CustomerForm", model);
                }

                await _userManager.AddToRoleAsync(generatedUser, "Customer");
                await _emailService.SendWelcomeEmailAsync(generatedUser.Email!, $"{generatedUser.FirstName} {generatedUser.LastName}", cancellationToken);
                ownerUserId = generatedUser.Id;
            }
        }

        var entity = new CustomerBusiness
        {
            Name = model.Name,
            VatNumber = model.VatNumber,
            FiscalCode = model.FiscalCode,
            Email = model.Email,
            Phone = model.Phone,
            Address = model.Address,
            City = model.City,
            Province = model.Province,
            Description = model.Description,
            LogoPath = await SaveImageAsync(model.LogoFile, "customers", cancellationToken),
            OwnerUserId = ownerUserId
        };

        _db.CustomerBusinesses.Add(entity);
        await _db.SaveChangesAsync(cancellationToken);

        TempData["Success"] = autoGeneratedPassword is null
            ? "Cliente creato con successo."
            : $"Cliente creato con successo. Account proprietario generato automaticamente con email {model.Email} e password temporanea: {autoGeneratedPassword}";
        return RedirectToAction(nameof(Customers));
    }

