    [HttpPost]
    [ValidateAntiForgeryToken]
    public async Task<IActionResult> NewCoupon(AdminCouponFormViewModel model, CancellationToken cancellationToken)
    {
        await PopulateCouponDependenciesAsync(model, cancellationToken);
        model.ExistingImages = [];
        if (!ModelState.IsValid)
            return View("CouponForm", model);

        var exists = await _db.Coupons.AnyAsync(x => x.Slug == model.Slug, cancellationToken);
        if (exists)
        {
            ModelState.AddModelError(nameof(model.Slug), "Slug già esistente.");
            return View("CouponForm", model);
        }

        var entity = new Coupon
        {
            CustomerBusinessId = model.CustomerBusinessId,
            CouponCategoryId = model.CouponCategoryId,
            Title = model.Title,
            Slug = model.Slug,
            ShortDescription = model.ShortDescription,
            FullDescription = model.FullDescription,
            OriginalPrice = model.OriginalPrice,
            DiscountedPrice = model.DiscountedPrice,
            CouponType = model.CouponType,
            Status = model.Status,
            ValidFrom = model.ValidFrom,
            ValidTo = model.ValidTo,
            MaxDownloads = model.MaxDownloads,
            IsFeatured = model.IsFeatured,
            IsBoostedInHome = model.IsBoostedInHome
        };

        _db.Coupons.Add(entity);
        await _db.SaveChangesAsync(cancellationToken);

        if (model.ImageFiles.Count > 0)
        {
            var couponImages = new List<CouponImage>();
            for (var i = 0; i < model.ImageFiles.Count; i++)
            {
                var filePath = await SaveImageAsync(model.ImageFiles[i], "coupons", cancellationToken);
                if (string.IsNullOrWhiteSpace(filePath))
                    continue;
                couponImages.Add(new CouponImage
                {
                    CouponId = entity.Id,
                    FilePath = filePath,
                    IsPrimary = i == 0
                });
            }

            _db.CouponImages.AddRange(couponImages);
            await _db.SaveChangesAsync(cancellationToken);
        }

        if (entity.Status == CouponStatus.Active)
        {
            var business = await _db.CustomerBusinesses.AsNoTracking()
                .FirstOrDefaultAsync(x => x.Id == entity.CustomerBusinessId, cancellationToken);
            if (!string.IsNullOrWhiteSpace(business?.Email))
            {
                await _emailService.SendCouponPublishedEmailAsync(
                    business.Email,
                    business.Name,
                    entity.Title,
                    entity.ValidTo,
                    cancellationToken);
            }
        }

        TempData["Success"] = "Coupon creato con successo.";
        return RedirectToAction(nameof(Coupons));
    }

