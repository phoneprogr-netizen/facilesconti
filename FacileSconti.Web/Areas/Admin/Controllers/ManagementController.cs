    [HttpPost]
    [ValidateAntiForgeryToken]
    public async Task<IActionResult> EditCoupon(AdminCouponFormViewModel model, CancellationToken cancellationToken)
    {
        if (model.Id is null) return NotFound();

        var entity = await _db.Coupons
            .Include(x => x.Images)
            .FirstOrDefaultAsync(x => x.Id == model.Id.Value, cancellationToken);
        if (entity is null) return NotFound();

        model.ExistingImages = entity.Images
            .OrderByDescending(x => x.IsPrimary)
            .ThenBy(x => x.Id)
            .Select(x => new AdminCouponImageItemViewModel
            {
                Id = x.Id,
                FilePath = x.FilePath,
                IsPrimary = x.IsPrimary
            })
            .ToList();

        await PopulateCouponDependenciesAsync(model, cancellationToken);
        if (!ModelState.IsValid)
            return View("CouponForm", model);

        var duplicatedSlug = await _db.Coupons.AnyAsync(x => x.Id != model.Id.Value && x.Slug == model.Slug, cancellationToken);
        if (duplicatedSlug)
        {
            ModelState.AddModelError(nameof(model.Slug), "Slug già esistente.");
            return View("CouponForm", model);
        }

        entity.CustomerBusinessId = model.CustomerBusinessId;
        entity.CouponCategoryId = model.CouponCategoryId;
        entity.Title = model.Title;
        entity.Slug = model.Slug;
        entity.ShortDescription = model.ShortDescription;
        entity.FullDescription = model.FullDescription;
        entity.OriginalPrice = model.OriginalPrice;
        entity.DiscountedPrice = model.DiscountedPrice;
        entity.CouponType = model.CouponType;
        var previousStatus = entity.Status;
        entity.Status = model.Status;
        entity.ValidFrom = model.ValidFrom;
        entity.ValidTo = model.ValidTo;
        entity.MaxDownloads = model.MaxDownloads;
        entity.IsFeatured = model.IsFeatured;
        entity.IsBoostedInHome = model.IsBoostedInHome;

        var removeIds = model.RemoveImageIds.Distinct().ToHashSet();
        if (removeIds.Count > 0)
        {
            var imagesToRemove = entity.Images.Where(x => removeIds.Contains(x.Id)).ToList();
            _db.CouponImages.RemoveRange(imagesToRemove);
        }

        if (model.ImageFiles.Count > 0)
        {
            foreach (var imageFile in model.ImageFiles)
            {
                var filePath = await SaveImageAsync(imageFile, "coupons", cancellationToken);
                if (string.IsNullOrWhiteSpace(filePath))
                    continue;
                entity.Images.Add(new CouponImage
                {
                    FilePath = filePath,
                    IsPrimary = false
                });
            }
        }

        if (entity.Images.All(x => !x.IsPrimary))
        {
            var firstImage = entity.Images.FirstOrDefault();
            if (firstImage is not null)
                firstImage.IsPrimary = true;
        }

        await _db.SaveChangesAsync(cancellationToken);

        if (previousStatus != CouponStatus.Active && entity.Status == CouponStatus.Active)
        {
            var business = await _db.CustomerBusinesses.AsNoTracking()
                .FirstOrDefaultAsync(x => x.Id == entity.CustomerBusinessId, cancellationToken);
            if (!string.IsNullOrWhiteSpace(business?.Email))
            {
                await _emailService.SendCouponPublishedEmailAsync(
                    business.Email,
                    business.Name,
                    entity.Title,
                    entity.ValidTo,
                    cancellationToken);
            }
        }

        TempData["Success"] = "Coupon aggiornato con successo.";
        return RedirectToAction(nameof(Coupons));
    }

